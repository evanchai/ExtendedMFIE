package cn.edu.fudan.se.web.bean;

public class CallBean
{
	private int count=0;	
	private String callClass="";
	
	public int getCount()
	{
		return count;
	}
	public void setCount(int count)
	{
		this.count = count;
	}
	public String getCallClass()
	{
		return callClass;
	}
	public void setCallClass(String callClass)
	{
		this.callClass = callClass;
	}
}
